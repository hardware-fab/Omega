int main(int argc, char **argv) {

    printf("****start*****\n");

    /* <<--params-->> */
	 const unsigned sha_vsize = 2;
	 const unsigned sha_blocksize = 8192;
	 const unsigned sha_digest = 20;
	 const unsigned sha_n = 10;

    uint32_t in_words_adj;
    uint32_t out_words_adj;
    uint32_t in_size;
    uint32_t out_size;
    uint32_t dma_in_size;
    uint32_t dma_out_size;
    uint32_t dma_size;


    in_words_adj = round_up(sha_vsize*sha_blocksize, VALUES_PER_WORD);
    out_words_adj = round_up(sha_digest, VALUES_PER_WORD);
    in_size = in_words_adj * (sha_n);
    out_size = out_words_adj * (sha_n);

    dma_in_size = in_size / VALUES_PER_WORD;
    dma_out_size = out_size / VALUES_PER_WORD;
    dma_size = dma_in_size + dma_out_size;

    dma_word_t *mem=(dma_word_t*) malloc(dma_size * sizeof(dma_word_t));
    word_t *inbuff=(word_t*) malloc(in_size * sizeof(word_t));
    word_t *outbuff=(word_t*) malloc(out_size * sizeof(word_t));
    word_t *outbuff_gold= (word_t*) malloc(out_size * sizeof(word_t));
    dma_info_t load;
    dma_info_t store;

    // Prepare input data
    for(unsigned i = 0; i < sha_n; i++)
        for(unsigned j = 0; j < sha_vsize*sha_blocksize; j++)
        {
            inbuff[i * in_words_adj + j] = (word_t) indata[j];
            //printf("iter:%d    -    %d: in=%02x\n", i, j, (BYTE) inbuff[i * in_words_adj + j] );
        }

    for(unsigned i = 0; i < dma_in_size; i++)
	for(unsigned k = 0; k < VALUES_PER_WORD; k++)
	    mem[i].word[k] = inbuff[i * VALUES_PER_WORD + k];

    // Set golden output
    for (unsigned i = 0; i < sha_n; i++)
        sha_main_sw((BYTE *)&inbuff[i*in_words_adj], (BYTE *)&outbuff_gold[i*out_words_adj]);


    // Call the TOP function
    top(mem, mem,
        /* <<--args-->> */
	 	 sha_vsize,
	 	 sha_blocksize,
	 	 sha_digest,
	 	 sha_n,
        load, store);

    // Validate
    uint32_t out_offset = dma_in_size;
    for(unsigned i = 0; i < dma_out_size; i++)
	for(unsigned k = 0; k < VALUES_PER_WORD; k++)
	    outbuff[i * VALUES_PER_WORD + k] = mem[out_offset + i].word[k];

    int errors = 0;
    for(unsigned i = 0; i < sha_n; i++)
        for(unsigned j = 0; j < sha_digest; j++)
	    {
            if (outbuff[i * out_words_adj + j] != outbuff_gold[i * out_words_adj + j])
		        errors++;
            printf("iter:%d    -    %d: dut=%02x gold=%02x official=%08x \n", i, j, (BYTE) outbuff[i * out_words_adj + j], (BYTE) outbuff_gold[i * out_words_adj + j], outData[j/4]);
        }

    if (errors)
	std::cout << "Test FAILED with " << errors << " errors." << std::endl;
    else
	std::cout << "Test PASSED." << std::endl;

    // Free memory

    free(mem);
    free(inbuff);
    free(outbuff);
    free(outbuff_gold);

    return 0;
}
